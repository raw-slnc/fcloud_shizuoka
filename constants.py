def _build_nendo_list():
    items = []
    for y in range(datetime.date.today().year, 2005, -1):
        if y >= 2019:
            wareki = f'令和{y - 2018}年度'
        else:
            wareki = f'平成{y - 1988}年度'
        items.append((f'{wareki} ({y})', wareki))
    return items


_NENDO_LIST = _build_nendo_list()
